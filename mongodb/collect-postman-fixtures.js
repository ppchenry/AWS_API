#!/usr/bin/env node
"use strict";

const fs = require("fs");
const path = require("path");
const mongoose = require("mongoose");

function readEnvJson() {
  const p = path.resolve(process.cwd(), "env.json");
  if (!fs.existsSync(p)) return {};
  try {
    return JSON.parse(fs.readFileSync(p, "utf8"));
  } catch {
    return {};
  }
}

function pickPrimaryUri(envJson) {
  return (
    process.env.MONGODB_URI ||
    envJson?.OrderVerification?.MONGODB_URI ||
    envJson?.UserRoutesFunction?.MONGODB_URI ||
    envJson?.PetDetailInfoFunction?.MONGODB_URI ||
    ""
  );
}

function pickGetAllPetsUri(envJson, primaryUri) {
  return (
    process.env.GET_ALL_PETS_MONGODB_URI ||
    envJson?.GetAllPetsFunction?.MONGODB_URI ||
    primaryUri
  );
}

function asStringId(v) {
  if (!v) return "";
  if (typeof v === "string") return v;
  if (typeof v.valueOf === "function") return String(v.valueOf());
  return String(v);
}

async function connect(uri, dbLabel) {
  const conn = mongoose.createConnection(uri, {
    serverSelectionTimeoutMS: 15000,
    connectTimeoutMS: 15000,
    socketTimeoutMS: 15000,
    maxPoolSize: 5,
  });
  await conn.asPromise();
  return conn;
}

async function getOwnerFixture(conn) {
  const docs = await conn.db.collection("pets").aggregate([
    { $match: { deleted: { $ne: true }, userId: { $exists: true, $ne: null } } },
    {
      $lookup: {
        from: "users",
        localField: "userId",
        foreignField: "_id",
        as: "user",
      },
    },
    { $unwind: "$user" },
    {
      $match: {
        "user.deleted": { $ne: true },
        "user.email": { $type: "string", $ne: "" },
      },
    },
    { $sort: { updatedAt: -1, _id: -1 } },
    {
      $project: {
        _id: 0,
        userId: "$userId",
        userEmail: { $toLower: "$user.email" },
        petId: "$_id",
        image: "$user.image",
      },
    },
    { $limit: 1 },
  ]).toArray();

  if (!docs.length) return null;
  const d = docs[0];
  return {
    TEST_OWNER_USER_ID: asStringId(d.userId),
    TEST_OWNER_EMAIL: d.userEmail || "",
    TEST_PET_ID: asStringId(d.petId),
    TEST_IMAGE_URL: typeof d.image === "string" && d.image ? d.image : "",
  };
}

async function getNgoFixtures(conn) {
  const rows = await conn.db.collection("pets").aggregate([
    { $match: { deleted: { $ne: true }, ngoId: { $exists: true, $ne: null, $ne: "" } } },
    { $project: { ngoId: { $toString: "$ngoId" } } },
    { $group: { _id: "$ngoId", count: { $sum: 1 } } },
    { $sort: { count: -1, _id: 1 } },
    { $limit: 2 },
  ]).toArray();

  const ngo1 = rows[0]?._id || "";
  const ngo2 = rows[1]?._id || ngo1;

  return { PUBLIC_NGO_ID: ngo1, PUBLIC_NGO_ID_ALT: ngo2 };
}

async function getTestNgoId(primaryConn, getAllPetsConn) {
  const ngoDoc =
    (await primaryConn.db.collection("ngos").findOne({}, { projection: { _id: 1 } })) ||
    (await getAllPetsConn.db.collection("ngos").findOne({}, { projection: { _id: 1 } }));
  return ngoDoc ? asStringId(ngoDoc._id) : "";
}

async function getTagFixture(primaryConn, getAllPetsConn) {
  const query = {
    deleted: { $ne: true },
    tagId: { $type: "string", $nin: ["", " "] },
  };
  const proj = { projection: { tagId: 1, _id: 0 } };
  const d1 = await primaryConn.db.collection("pets").findOne(query, proj);
  if (d1?.tagId) return d1.tagId;
  const d2 = await getAllPetsConn.db.collection("pets").findOne(query, proj);
  return d2?.tagId || "";
}

async function getOrderFixtures(primaryConn) {
  const rows = await primaryConn.db.collection("orderVerification").aggregate([
    {
      $match: {
        cancelled: { $ne: true },
        tagId: { $type: "string", $ne: "" },
        orderId: { $type: "string", $ne: "" },
      },
    },
    {
      $lookup: {
        from: "order",
        localField: "orderId",
        foreignField: "tempId",
        as: "o",
      },
    },
    { $unwind: "$o" },
    { $match: { "o.email": { $type: "string", $ne: "" } } },
    { $sort: { updatedAt: -1, _id: -1 } },
    {
      $project: {
        _id: 0,
        ORDER_VERIFICATION_ID: { $toString: "$_id" },
        ORDER_VERIFICATION_TAG_ID: "$tagId",
        ORDER_ID: "$orderId",
        ORDER_TEMP_ID: "$o.tempId",
        ORDER_OWNER_EMAIL: { $toLower: "$o.email" },
      },
    },
    { $limit: 1 },
  ]).toArray();

  if (!rows.length) return null;
  const base = rows[0];
  const owner = await primaryConn.db.collection("users").findOne(
    { email: base.ORDER_OWNER_EMAIL, deleted: { $ne: true } },
    { projection: { _id: 1 } }
  );

  return {
    ...base,
    ORDER_OWNER_USER_ID: owner ? asStringId(owner._id) : "",
  };
}

function printResult(lines) {
  process.stdout.write("# Generated by mongodb/collect-postman-fixtures.js\n");
  process.stdout.write("# Copy the lines below into your .env\n\n");
  for (const line of lines) process.stdout.write(`${line}\n`);
}

async function main() {
  const envJson = readEnvJson();
  const primaryUri = pickPrimaryUri(envJson);
  const getAllPetsUri = pickGetAllPetsUri(envJson, primaryUri);

  if (!primaryUri) {
    throw new Error(
      "No Mongo URI found. Set MONGODB_URI or keep env.json with OrderVerification/UserRoutes URI."
    );
  }

  let primaryConn;
  let getAllPetsConn;
  try {
    primaryConn = await connect(primaryUri, "primary");
    getAllPetsConn = getAllPetsUri === primaryUri ? primaryConn : await connect(getAllPetsUri, "getallpets");

    const owner = await getOwnerFixture(primaryConn);
    const ngoPublic = await getNgoFixtures(getAllPetsConn);
    const testNgoId = await getTestNgoId(primaryConn, getAllPetsConn);
    const tagId = await getTagFixture(primaryConn, getAllPetsConn);
    const order = await getOrderFixtures(primaryConn);

    if (!owner) {
      throw new Error("No owner/pet fixture found in primary DB (pets+users join returned empty).");
    }

    const lines = [
      `TEST_OWNER_USER_ID=${owner.TEST_OWNER_USER_ID}`,
      `TEST_OWNER_EMAIL=${owner.TEST_OWNER_EMAIL}`,
      `TEST_PET_ID=${owner.TEST_PET_ID}`,
      `TEST_NGO_ID=${testNgoId}`,
      `PUBLIC_NGO_ID=${ngoPublic.PUBLIC_NGO_ID}`,
      `PUBLIC_NGO_ID_ALT=${ngoPublic.PUBLIC_NGO_ID_ALT}`,
      `TEST_IMAGE_URL=${owner.TEST_IMAGE_URL || ""}`,
      `TEST_TAG_ID=${tagId || ""}`,
      `ORDER_VERIFICATION_ID=${order?.ORDER_VERIFICATION_ID || ""}`,
      `ORDER_VERIFICATION_TAG_ID=${order?.ORDER_VERIFICATION_TAG_ID || ""}`,
      `ORDER_ID=${order?.ORDER_ID || ""}`,
      `ORDER_TEMP_ID=${order?.ORDER_TEMP_ID || ""}`,
      `ORDER_OWNER_EMAIL=${order?.ORDER_OWNER_EMAIL || ""}`,
      `ORDER_OWNER_USER_ID=${order?.ORDER_OWNER_USER_ID || ""}`,
    ];

    printResult(lines);
  } finally {
    const close = async (conn) => {
      if (!conn) return;
      try { await conn.close(); } catch { /* ignore */ }
    };
    if (getAllPetsConn && getAllPetsConn !== primaryConn) await close(getAllPetsConn);
    await close(primaryConn);
  }
}

main().catch((err) => {
  console.error(`[fixture-script] ${err.message}`);
  process.exit(1);
});

